package com.apsidiscount.service;

public class ApsiDiscountException extends Exception {

	/**
	 * 
	 */
	private static final long serialVersionUID = 1L;

	public ApsiDiscountException() {
	}

	public ApsiDiscountException(String message, Throwable cause) {
		super(message, cause);
	}

	public ApsiDiscountException(String message) {
		super(message);
	}

	public ApsiDiscountException(Throwable cause) {
		super(cause);
	}
	
}
